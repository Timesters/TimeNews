				<p>Pencipta karakter Goku dan lainnya ini meninggal akibat hematuroma subdural akut atau gumpalan darah secara tiba-tiba pada permukaan otak, akibat cedera kepala.</p>
				<p>Kabar meninggalnya Akira Toriyama membawa duka tersendiri bagi kreator manga lainnya, seperti pencipta One Piece Eiichiro Oda dan pencipta Naruto Masashi Kishimoto.<p>
				<p>Eiichiro Oda menulis dalam sebuah pernyataan melalui Shonen Jump: “Ini terlalu dini. Lubang yang tersisa terlalu besar. Ketika aku berpikir bahwa aku tidak akan pernah bertemu dengannya lagi, kesedihan melanda diriku. Saya sangat mengaguminya sejak saya masih kecil, dan saya bahkan ingat hari pertama dia memanggil nama saya. Saya juga sangat ingat hari ketika kami berjalan pulang setelah dia menggunakan kata ‘teman’ untuk menyebut kami, dan hari ketika kami bersenang-senang bersama Kishimoto. Saya juga ingat percakapan terakhir kami.”</p>
				<p>“Dia mengambil tongkat estafet dari era di mana orang-orang diberitahu bahwa membaca manga akan membuat mereka bodoh, dan dia adalah salah satu orang yang menciptakan era di mana orang dewasa dan anak-anak dapat menikmati membaca manga. Dia menunjukkan kepada kita bahwa manga dapat melakukan banyak hal, dan dapat membawa kita ke dunia nyata. Rasanya seperti menyaksikan seorang pahlawan menyerang ke depan. Bukan hanya bagi para seniman manga, keseruan dan emosi era serialisasi Dragon Ball harus berakar pada masa kecil para kreator di semua industri. Keberadaannya bagaikan pohon besar,” tutur Eiichiro Oda. Sementara, pencipta Naruto Masashi Kishimoto menulis dalam sebuah pernyataan, juga melalui Shonen Jump: “Saya tumbuh dengan manga Mr. Toriyama, Dr. Slump di sekolah dasar dan Dragon Ball di sekolah menengah. Wajar bagi saya untuk memiliki manga Pak Toriyama di samping saya sebagai bagian dari hidup saya. Bahkan saat aku sedang sedih, acara mingguan Dragon Ball selalu membuatku melupakannya. Itu adalah keselamatan bagi saya, seorang anak desa yang tidak melakukan apa-apa. Itulah betapa aku menikmati Dragon Ball!”</p>
				<p>Sementara, pencipta Naruto Masashi Kishimoto menulis dalam sebuah pernyataan, juga melalui Shonen Jump: “Saya tumbuh dengan manga Mr. Toriyama, Dr. Slump di sekolah dasar dan Dragon Ball di sekolah menengah. Wajar bagi saya untuk memiliki manga Pak Toriyama di samping saya sebagai bagian dari hidup saya. Bahkan saat aku sedang sedih, acara mingguan Dragon Ball selalu membuatku melupakannya. Itu adalah keselamatan bagi saya, seorang anak desa yang tidak melakukan apa-apa. Itulah betapa aku menikmati Dragon Ball!”</p>
				<p>Kishimoto menambahkan: “Dengan mengejar Tuan Toriyama, saya bisa menemukan kegembiraan baru. Tuan Toriyama selalu menjadi kompas saya. Dia adalah inspirasi saya. Saya mungkin mengganggu Tuan Toriyama, tapi saya berterima kasih padanya tanpa izin. Bagiku, dia adalah penyelamat dan dewa manga.”</p>
				<p>“Saya baru saja menerima kabar meninggalnya Tuan Toriyama. Aku diliputi oleh rasa kehilangan yang luar biasa, bahkan lebih besar dibandingkan saat Dragon Ball berakhir… Aku belum tahu bagaimana cara mengatasi lubang di hatiku ini. Saya tidak bisa membaca Dragon Ball favorit saya saat ini. Saya bahkan merasa tidak menulis teks ini dengan benar kepada Tuan Toriyama. Semua orang di dunia masih menantikan karya Tuan Toriyama. Jika satu keinginan Dragon Ball benar-benar terkabul… Maaf… Ini mungkin egois, tapi saya sedih, Tuan Toriyama,” tutur Kishimoto.</p>
